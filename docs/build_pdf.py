def callout_panel(text: str, *, color=TEAL, fill=PANEL_SOFT,
                  width=17 * cm) -> Table:
    """Branded callout box for important notes."""
    p = Paragraph(render_inline(text), STYLES["body"])
    table = Table([[p]], colWidths=[width])
    table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, -1), fill),
                ("BOX", (0, 0), (-1, -1), 1.2, color),
                ("LEFTPADDING", (0, 0), (-1, -1), 14),
                ("RIGHTPADDING", (0, 0), (-1, -1), 14),
                ("TOPPADDING", (0, 0), (-1, -1), 12),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 12),
            ]
        )
    )
    return table


def numbered_section(number: str, title: str) -> Table:
    """Big colored section header with circled number badge."""
    badge = Paragraph(
        f'<para alignment="center"><font color="#FFFFFF" size="22"><b>{number}</b></font></para>',
        STYLES["body"],
    )
    title_para = Paragraph(
        f'<para alignment="left"><font color="#0F766E" size="26"><b>{escape(title)}</b></font></para>',
        STYLES["body"],
    )
    table = Table(
        [[badge, title_para]],
        colWidths=[1.8 * cm, 15.2 * cm],
        rowHeights=[1.8 * cm],
    )
    table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (0, 0), TEAL),
                ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                ("LEFTPADDING", (0, 0), (-1, -1), 0),
                ("RIGHTPADDING", (0, 0), (-1, -1), 0),
                ("TOPPADDING", (0, 0), (-1, -1), 0),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 0),
                ("LEFTPADDING", (1, 0), (1, 0), 16),
            ]
        )
    )
    return table


