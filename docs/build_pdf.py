# ------------------------------------------------------------------------ fonts
def register_fonts() -> tuple[str, str, str]:
    candidates = [
        ("Calibri", "calibri.ttf", "calibrib.ttf", "calibrii.ttf"),
        ("Segoe UI", "segoeui.ttf", "segoeuib.ttf", "segoeuii.ttf"),
        ("Arial", "arial.ttf", "arialbd.ttf", "ariali.ttf"),
    ]
    windows_fonts = Path("C:/Windows/Fonts")
    for name, reg, bold, italic in candidates:
        r = windows_fonts / reg
        b = windows_fonts / bold
        if r.exists() and b.exists():
            pdfmetrics.registerFont(TTFont(name, str(r)))
            pdfmetrics.registerFont(TTFont(f"{name}-Bold", str(b)))
            it = windows_fonts / italic
            pdfmetrics.registerFont(
                TTFont(f"{name}-Italic", str(it if it.exists() else r))
            )
            return name, f"{name}-Bold", f"{name}-Italic"
    return "Helvetica", "Helvetica-Bold", "Helvetica-Oblique"


