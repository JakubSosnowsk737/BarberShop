# ------------------------------------------------------------------------ inline
INLINE_RE = re.compile(r"(\*\*[^*]+\*\*|\*[^*]+\*|`[^`]+`|_[^_]+_)")


def escape(text: str) -> str:
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
    )


def render_inline(text: str) -> str:
    parts = []
    for chunk in INLINE_RE.split(text):
        if not chunk:
            continue
        if chunk.startswith("**") and chunk.endswith("**"):
            parts.append(f"<b>{escape(chunk[2:-2])}</b>")
        elif chunk.startswith("*") and chunk.endswith("*"):
            parts.append(f"<i>{escape(chunk[1:-1])}</i>")
        elif chunk.startswith("_") and chunk.endswith("_"):
            parts.append(f"<i>{escape(chunk[1:-1])}</i>")
        elif chunk.startswith("`") and chunk.endswith("`"):
            parts.append(f"<b>{escape(chunk[1:-1])}</b>")
        else:
            parts.append(escape(chunk))
    return "".join(parts)


